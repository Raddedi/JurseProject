                      <p>The Joint Urban Remote Sensing Event (JURSE) is a forum of excellence where researchers, practitioners and students present, share, and discuss their latest findings and results. A very dynamic version of the JURSE organized by the Remote Sensing Lab of FORTH in Heraklion, Tunisia in May 2025.<br><br>

                        This event is committed to introduce innovative methodologies and technological resources recently employed to investigate the manifold aspects of the urban environment through orbital and airborne remote sensing data.<br><br>

                        Emerging topics like new methods for urban land cover and land use classification with detailed discrimination of urban targets, 3D modeling of urban buildings, forecast and impact assessment of natural and man-made hazards in urban areas, urban social studies, urban ecology, urban climatology, as well as data fusion, algorithms and techniques for Earth Observation data interpretation were approached.</p>
